def calculate_mean(a, b):
    """
    calculate the mean of a and b
    :param a: first
    :param b: second
    :return: mean
    """
    return float(a + b) / 2


def calculate_power(base, exp):
    """
    Calculate the power of a raised to the given exponent
    :param base: base
    :param exp: exponent
    :return: power
    """
    return base ** exp
